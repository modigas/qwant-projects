#include "main.h"
#include <fcntl.h>

int main()
{
    int fd = 0;
    bool NewFile = false;

    fd = openFile(fd, &NewFile);
    initializePrompt();
    createBlockchain(fd, NewFile);

    close(fd);
    return 0;
}

int openFile(int fd, bool* NewFile)
{
    if ((fd = open("blockchain.txt", O_RDONLY | O_CREAT | O_EXCL, S_IRUSR | S_IWUSR | S_IRGRP | S_IROTH)) == -1)
    {
        // File already exists
        if ((fd = open("blockchain.txt", O_RDWR | O_CREAT, S_IRUSR | S_IWUSR | S_IRGRP | S_IROTH)) == -1) 
        {
            fprintf(stderr, "%s: Cannot open: Is a directory\n", "blockchain.txt");
            fprintf(stderr, "blockchain: Error is not recoverable: exiting now\n");
            exit(1);
        }
        *NewFile = false;
    } 
    else 
    {
        // New file
        if ((fd = open("blockchain.txt", O_WRONLY | O_CREAT, S_IRUSR | S_IWUSR | S_IRGRP | S_IROTH)) == -1)
        {
            fprintf(stderr, "%s: Cannot open: Is a directory\n", "blockchain.txt");
            fprintf(stderr, "blockchain: Error is not recoverable: exiting now\n");
            exit(1);
        }
        *NewFile = true;
    }
    return fd;
}

